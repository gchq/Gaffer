package gaffer.graph;

import java.io.Serializable;

public class TypeValue implements Serializable, Comparable<TypeValue> {

	private static final long serialVersionUID = -2585599878869936634L;
	private String type;
	private String value;
	
	public TypeValue(String type, String value) {
		this.type = type;
		this.value = value;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		TypeValue other = (TypeValue) obj;
		if (type == null) {
			if (other.type != null)
				return false;
		} else if (!type.equals(other.type))
			return false;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "TypeValue [type=" + type + ", value=" + value + "]";
	}

	@Override
	public int compareTo(TypeValue o) {
		if (!this.type.equals(o.type)) {
			return this.type.compareTo(o.type);
		}
		return this.value.compareTo(o.value);
	}
}
